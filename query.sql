CREATE TABLE CarSales (
    Branch_ID VARCHAR(100),
    Dealer_ID VARCHAR(100),
    Model_ID VARCHAR(100),
    Revenue BIGINT,
    Units_Sold INT,
    Date_ID VARCHAR(10),
    Day INT,
    Month INT,
    Year INT,
    BranchName VARCHAR(100),
    DealerName VARCHAR(100),
    Product_Name VARCHAR(100)
);